/**
 *_memcpy - copies memory area
 *@dest: storage
 *@src: memory where is copied
 *@n: number of bytes
 *
 *Return: copied memory with n byted
 */
char *_memcpy(char *dest, char *src, unsigned int n)
{
	int i;
	int j;
	
	i = 0;
	j = n;
	for (; i < i; i++)
	{
		dest[i] = src[i];
		n--;
	}
	return (dest);
}
